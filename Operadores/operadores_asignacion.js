//Asignacion =
x = y;   //x = y
let a = 1;   // a = 1
console.log(a);

//Asignacion de adicion +=
x += y;  //x = x+y
let b = 3; 
b += 5;  // b = 3 + 5
console.log(b);

//Asignacion de resta   -=
x -= y; //x = x-y
let c = 4;
c -= 1; //c = 4 + 1
console.log(c); 

//Asignacion de multiplicacion  *=
x *= y; //x = x*y
let d = 5;
d *= 2; //d = 5 * 2
console.log(d);

//Asignacion de division    /=
x /= y; //x= x/y
let e = 50;
e /= 2;  //e = 50 / 2
console.log(e);

//Asignacion de residuo %=
x %= y; //x = x%y
let f = 10;
f %= 3;  //f = 10 % 3
console.log(f);

//Asignacion de exponenciacion  **=
x **= y; //x = x**y
let g = 2;
g **= 3;    //g = 2 ** 3
console.log(g);

